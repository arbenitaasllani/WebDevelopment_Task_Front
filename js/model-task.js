class Task {
  constructor(
    id,
    taskName,
    description,
    status,
    dateAdded,
    estimatedFinishDate
  ) {
    this.id = id;
    this.taskName = taskName;
    this.description = description;
    this.status = status;
    this.dateAdded = dateAdded;
    this.estimatedFinishDate = estimatedFinishDate;
  }
}
